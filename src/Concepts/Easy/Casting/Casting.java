package Concepts.Easy.Casting;

public class Casting {

    public static void main(String[] args) {
        // - You should cast when you want the compiler to interpret a type a certain way.
        // - Sometimes the compiler might truncate values to make the casting work out.
        System.out.println((int) 5.4); // 5
        System.out.println((double) 5); // 5.0
    }
}

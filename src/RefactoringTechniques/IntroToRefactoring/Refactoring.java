package RefactoringTechniques.IntroToRefactoring;

public class Refactoring {

    // What is refactoring?
    // - improving the design of your code while preserving behaviour
    // (presering behaviour is important -- otherwise you're either debugging, adding new
    // features, removing old features, or modifying existing features)
    // - refactoring makes other people's code, and your existing code easier to understand
    // - refactoring lets you efficiently extend or modify your program while
    // minimizing the risk of introducing new bugs.
}
